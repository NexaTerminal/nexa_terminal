/** Returns the feature config with the common sections appended. */
export function getFeatureTerms(feature) {
  const cfg = FEATURE_TERMS[feature];
  if (!cfg) return null;
  return { ...cfg, sections: [...cfg.sections, ...COMMON_SECTIONS] };
}
