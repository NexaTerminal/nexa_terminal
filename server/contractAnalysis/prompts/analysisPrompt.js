function buildAnalysisMessages({ contractText, userRole, userAnswers, contractType, parties, stancePrefix = '' }) {
  const userBlock = [
    `Тип на договор (детектирано): ${contractType || 'непознато'}`,
    `Улога на корисникот (КОГО ГО ЗАСТАПУВАШ): ${userRole || 'непознато'}`,
    parties && parties.length
      ? `Страни во договорот:\n${parties.map(p => ` - ${p.label} (${p.role})`).join('\n')}`
      : null,
    userAnswers && Object.keys(userAnswers).length
      ? `Дополнителни одговори од корисникот:\n${Object.entries(userAnswers).map(([k, v]) => ` - ${k}: ${v}`).join('\n')}`
      : null,
    '',
    'ТЕКСТ НА ДОГОВОРОТ:',
    '---',
    contractText,
    '---',
    '',
    'Изврши темелна анализа како застапник на корисникот и врати JSON според специфицираниот формат. Не прескокнувај ниту едно од задолжителните полиња (termination, penalties, licenses, liability, legalRisks, commercialRisks).',
  ]
    .filter(Boolean)
    .join('\n');

  return [
    { role: 'system', content: (stancePrefix || '') + ANALYSIS_SYSTEM },
    { role: 'user',   content: userBlock },
  ];
}

