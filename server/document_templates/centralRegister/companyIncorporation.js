function agentPhrase(fd) {
  if ((fd.agentBlock || '').trim()) return fd.agentBlock.trim();
  const name = fd.agentName || '[НАЗИВ НА РЕГИСТРАЦИОНЕН АГЕНТ]';
  const addr = fd.agentAddress || '[АДРЕСА]';
  const persons = fd.agentPersons ? `, преку ${fd.agentPersons}` : '';
  return `го/ги овластувам регистрациониот агент ${name}, со седиште на ${addr}${persons}`;
}

// --------------------------------------------------------------------------- equipment table (non-monetary)
function cell(text, opts = {}) {
  return new TableCell({ children: [new Paragraph({ children: [new TextRun({ text: String(text == null ? '' : text), bold: opts.bold, font: FONT, size: 22 })] })] });
}
function tRow(cells, bold) { return new TableRow({ children: cells.map((c) => cell(c, { bold })) }); }
/** Parses "опрема | кол. | евра | денари" lines into rows. */
function parseEquipment(text) {
  return String(text || '').split('\n').map((l) => l.trim()).filter(Boolean).map((l) => {
    const c = l.split('|').map((s) => s.trim());
    return { item: c[0] || '', qty: c[1] || '', eur: c[2] || '', mkd: c[3] || '' };
  });
}
function equipmentTable(founder) {
  const rows = parseEquipment(founder.equipment);
  const trs = [tRow(['Ред. бр.', 'Опрема', 'Кол.', 'Проценета вредност ЕВРО', 'Проценета вредност ДЕНАРИ'], true)];
  rows.forEach((r, i) => trs.push(tRow([String(i + 1), r.item, r.qty, r.eur, r.mkd])));
  trs.push(tRow(['', 'Вкупно', '', founder.shareEUR || '', ''], true));
  return new Table({ width: { size: 100, type: WidthType.PERCENTAGE }, rows: trs });
}
function foundersSummaryTable(founders) {
  const trs = [tRow(['Ред. бр.', 'Содружници', 'ЕВРО', 'ДЕНАРИ'], true)];
  founders.forEach((f, i) => trs.push(tRow([String(i + 1), f.name, f.shareEUR || '', f.shareMKD || ''])));
  trs.push(tRow(['', 'ВКУПНА ВРЕДНОСТ НА ОСНОВНА ГЛАВНИНА', '', ''], true));
  return new Table({ width: { size: 100, type: WidthType.PERCENTAGE }, rows: trs });
}

