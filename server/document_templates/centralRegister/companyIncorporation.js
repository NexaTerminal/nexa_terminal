// --------------------------------------------------------------------------- paragraph helpers
function p(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text: text || '', bold: opts.bold, italics: opts.italics, font: FONT, size: opts.size || 24 })],
    alignment: opts.align || AlignmentType.JUSTIFIED,
    spacing: { after: opts.after != null ? opts.after : 200, line: LINE }
  });
}
function title(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text, bold: true, font: FONT, size: opts.size || 24 })],
    alignment: AlignmentType.CENTER,
    spacing: { after: opts.after != null ? opts.after : 200, line: LINE }
  });
}
function sectionHeader(text) {
  return new Paragraph({
    children: [new TextRun({ text, bold: true, font: FONT, size: 24 })],
    alignment: AlignmentType.CENTER,
    spacing: { before: 240, after: 120, line: LINE }
  });
}
function article(n) {
  return new Paragraph({
    children: [new TextRun({ text: `Член ${n}`, bold: true, font: FONT, size: 24 })],
    alignment: AlignmentType.CENTER,
    spacing: { before: 160, after: 100, line: LINE }
  });
}
function pageBreak() { return new Paragraph({ children: [new PageBreak()] }); }

/** Signature line + name(s), left aligned, no gap. */
function signature({ roleLabel, lines = [] }) {
  const out = [];
  if (roleLabel) {
    out.push(new Paragraph({
      children: [new TextRun({ text: roleLabel, bold: true, font: FONT, size: 24 })],
      alignment: AlignmentType.LEFT, spacing: { before: 300, after: 120, line: LINE }
    }));
  }
  out.push(new Paragraph({
    children: [new TextRun({ text: '___________________________', font: FONT, size: 24 })],
    alignment: AlignmentType.LEFT, spacing: { after: 0, line: LINE }
  }));
  lines.filter(Boolean).forEach((ln, i) => {
    out.push(new Paragraph({
      children: [new TextRun({ text: ln, font: FONT, size: 24 })],
      alignment: AlignmentType.LEFT, spacing: { after: i === lines.length - 1 ? 300 : 0, line: LINE }
    }));
  });
  return out;
}

// --------------------------------------------------------------------------- formatting
function fmtDate(value) { return value ? moment(value).format('DD.MM.YYYY') : moment().format('DD.MM.YYYY'); }

/** Parse a Macedonian/loose amount string to a number ("5.000,50" → 5000.5). */
function parseNum(v) {
  if (typeof v === 'number') return v;
  const n = parseFloat(String(v == null ? '' : v).replace(/\./g, '').replace(',', '.').replace(/[^\d.]/g, ''));
  return isNaN(n) ? null : n;
}
/** Macedonian number format: 1.000,00 (period thousands, comma decimal). */
function fmtAmount(value, decimals = 2) {
  const n = parseNum(value);
  if (n == null) return String(value || '');
  return n.toLocaleString('de-DE', { minimumFractionDigits: decimals, maximumFractionDigits: decimals });
}
function joinAnd(items) {
  const arr = items.filter(Boolean);
  if (arr.length === 0) return '';
  if (arr.length === 1) return arr[0];
  return arr.slice(0, -1).join(', ') + ' и ' + arr[arr.length - 1];
}

// --------------------------------------------------------------------------- person helpers
function normPerson(pr) {
  return {
    personType: pr.personType || 'natural_domestic',
    name: pr.name || '[Име]',
    nameLat: pr.nameLat || '',
    country: pr.country || '',
    city: pr.city || '',
    address: pr.address || '',
    idNumber: pr.idNumber || '',
    entityRep: pr.entityRep || '',
    shareEUR: pr.shareEUR || '',
    sharePercent: pr.sharePercent || '',
    powers: pr.powers || 'unlimited',
    limitEUR: pr.limitEUR || '',
    equipment: pr.equipment || ''
  };
}

/** Master §5.1 person descriptor. */
function pPerson(pr) {
  if (pr.personType === 'legal_entity') {
    return `${pr.name}, со седиште на ${pr.address || '[седиште]'}, со ЕМБС ${pr.idNumber || '[ЕМБС]'}, претставувано од управителот ${pr.entityRep || '[застапник]'}`;
  }
  if (pr.personType === 'natural_foreign') {
    return `${pr.name}, државјанин на ${pr.country || '[држава]'}, со адреса на живеење: ${pr.address || '[адреса]'}, со број на лична карта/пасош: ${pr.idNumber || '[документ]'}`;
  }
  return `${pr.name} од ${pr.city || '[град]'}, со живеалиште на адреса: ${pr.address || '[адреса]'}, со ЕМБГ ${pr.idNumber || '[ЕМБГ]'}`;
}
/** Name line(s) under a signature. Legal entity → "за {назив}" + representative. */
function signatoryLines(pr) {
  if (pr.personType === 'legal_entity') return [`за ${pr.name}`, `управител ${pr.entityRep || '[застапник]'}`];
  return [pr.name];
}

