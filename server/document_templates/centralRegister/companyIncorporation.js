function assemblePack(ctx) {
  const { isDooel, founders, managers, contributionType } = ctx;
  const docs = [];

  // 1) Constitutive document
  docs.push(isDooel ? buildIzjavaZaOsnovanje(ctx) : buildDogovorZaOsnovanje(ctx));

  // 2) Acceptance / чл.29-32-183-231 statements
  if (isDooel) {
    const sole = founders[0];
    const soleIsManager = managers.length === 1 && (managers[0].name || '').trim().toLowerCase() === (sole.name || '').trim().toLowerCase();
    if (soleIsManager) {
      docs.push(buildDocB(ctx, sole));
    } else {
      docs.push(buildDocBJoint(ctx)); // founder чл.29/32 (single founder)
      managers.forEach((m) => docs.push(buildDocBMgr(ctx, m)));
    }
  } else {
    docs.push(buildDocBJoint(ctx));
    managers.forEach((m) => docs.push(buildDocBMgr(ctx, m)));
  }

  // 3) Non-monetary statements
  if (contributionType === 'non_monetary') {
    founders.forEach((f) => docs.push(buildDocC(ctx, f)));
    docs.push(buildDocF(ctx));
  }

  // 4) Per-founder POA + чл.7 statement
  founders.forEach((f) => docs.push(buildDocD(ctx, f)));
  founders.forEach((f) => docs.push(buildDocE(ctx, f)));

  // 5) ЗП образец (all managers)
  docs.push(buildDocZP(ctx));

  const children = [];
  docs.forEach((block, i) => { if (i > 0) children.push(pageBreak()); children.push(...block); });
  return children;
}

function buildContext(formData, company) {
  const isDooel = (formData.companyForm || 'dooel').toLowerCase() === 'dooel';
  let founders = (Array.isArray(formData.foundersList) ? formData.foundersList : []).filter((f) => f && f.name).map(normPerson);
  if (founders.length === 0) founders = [normPerson({ name: '[Основач]' })];

  const capitalEUR = (formData.capitalEUR || '5.000').trim();
  const capitalMKD = (formData.capitalMKD || '').trim() || fmtAmount(parseNum(capitalEUR) * EUR_TO_MKD);

  // Per-founder MKD share (derived for the summary table / agreement).
  founders = founders.map((f) => ({ ...f, shareMKD: f.shareEUR ? fmtAmount(parseNum(f.shareEUR) * EUR_TO_MKD) : '' }));

  let managers = (Array.isArray(formData.managersList) ? formData.managersList : []).filter((m) => m && m.name).map(normPerson);
  if (managers.length === 0) managers = [founders[0]]; // sole founder is the manager by default

  return {
    fd: formData,
    isDooel,
    contributionType: (formData.contributionType || 'monetary').toLowerCase() === 'non_monetary' ? 'non_monetary' : 'monetary',
    paymentTiming: (formData.paymentTiming || 'at_incorporation').toLowerCase() === 'within_one_year' ? 'within_one_year' : 'at_incorporation',
    capitalEUR, capitalMKD,
    capitalWords: formData.capitalWords || '',
    nkdActivity: formData.nkdActivity || '[НКД шифра и назив]',
    businessClause: formData.businessClause || 'Општа клаузула за бизнис.',
    appraiser: formData.appraiser || '[ОВЛАСТЕН ПРОЦЕНУВАЧ]',
    specialClauses: formData.specialClauses || '',
    date: fmtDate(formData.date),
    founders, managers,
    co: {
      fullName: formData.companyFullName || company?.companyName || '[Назив на друштвото]',
      shortName: formData.companyShortName || '[Скратен назив]',
      foreignName: formData.companyForeignName || '[Назив во странство]',
      seat: formData.companySeat || company?.companyAddress || company?.address || '[Седиште]',
      city: formData.companyCity || 'Скопје'
    }
  };
}

function generateCompanyIncorporationDoc(formData = {}, user, company) {
  const ctx = buildContext(formData, company);
  const doc = new Document({
    creator: 'Nexa Terminal',
    title: 'Основање на фирма',
    sections: [{
      properties: { page: { margin: { top: 1134, right: 1134, bottom: 1134, left: 1134 } } },
      children: assemblePack(ctx)
    }]
  });
  return { doc };
}

